function get_city (ci) {
  // console.log(dct)
  var city_dct = dct[ci]
  var data = []
  for (i in years){
    var lst_year = city_dct[years[i]]
    var dct_c = {}
    dct_c['data'] = lst_year
    data.push(dct_c)
  }
  return data
}

function get_options (c) {
    var dt = []
    for (city in c){
      // console.log(c[city])
      var dct_o = {}
      dct_o['title'] = {text: c[city]}
      dct_o['series'] = get_city(c[city])
      // console.log(dct)
      dt.push(dct_o)
    }
    console.log(dt)
    return dt
}

